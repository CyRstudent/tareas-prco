tempc = float(input("Introduzca la temperatura en grados Celsius que quiera convertir a Farenheit: ")) #El usuario introduce la temperatura en celsius, y la convierte a un número decimal
tempf = (tempc*(9/5)) + 32 #Siguiendo la formula de conversión
print(f'La temperatura introducida en celsius ({tempc}ºC) convertida a farenheit son {tempf}ºF')
